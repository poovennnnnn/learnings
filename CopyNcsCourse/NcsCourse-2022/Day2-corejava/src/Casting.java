
public class Casting {

	public static void main(String[] args) {
	
		byte v1 = 100;
		int v2 = 400;
		v2 = (byte) v1;
		
		int x1 = 100;
		long x2 = 2000;
		x1 = (int) x2;
		
	    short s1 = 100;
	    double s2 = 2000;
	    s1 = (short)s2 ;
	    
	    float f1 = 10f;
	    double f2 = 10.09;
	    f1 = (float)f2;
	    

	}

}
