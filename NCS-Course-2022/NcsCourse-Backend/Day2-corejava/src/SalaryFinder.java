
public class SalaryFinder {
	void showSalary(String designation) {
		switch(designation) {
		case "manager":{
			System.out.println("Salary is 8k");
			break;
		}
		case "clerk":{
			System.out.println("Salary is 5k");
			break;
		}
		case "accountant":{
			System.out.println("Salary is 6k");
			break;
		}
		case "CEO":{
			System.out.println("Salary is 123k");
			break;
		}
		
		}
	}

}
